		<h2>PACTO ECOLÓGICO EUROPEU</h2>
		<p>
			O Pacto Ecológico Europeu é a estratégia de crescimento da UE. Lançado em 2019, consiste num
			pacote de iniciativas estratégicas que colocou a UE na via rumo a uma transição ecológica, com
			o objetivo último de alcançar a neutralidade climática até 2050. Promove uma transição para
			energias limpas que proteja as pessoas e o planeta e seja economicamente robusta e socialmente
			justa, sublinhando a necessidade de todos os domínios políticos contribuírem para a luta
			contra as alterações climáticas. A estratégia apoia medidas em todos os setores económicos que
			abrangem a energia, os transportes, a indústria, a agricultura e o financiamento sustentável,
			entre outros. Para mais informações consulte o Pacto <a
