			de forma integrada com as preocupações ambientais. A AIA avalia os efeitos ambientais e
			sociais de propostas de desenvolvimento, normalmente projetos, em relação à sua localização,
			dimensão, natureza e características. Uma das grandes vantagens da AAE é reduzir a incerteza
