		<h3>Reunião nº1</h3>
		<h4>Principais pontos discutidos:</h4>
		<p>A discussão do focus group centrou-se na seguinte questão:</p>
		<p>
			Quais os mecanismos a adotar que proporcionem/potenciem a simplificação e aceleração dos
			licenciamentos elétrico, ambiental e urbanístico dos projetos de produção de energia renovável
			– solar e eólica, em ZAER?
		</p>
		<p>E, nesse âmbito:</p>
		<ol>
			<li>
				Quais os principais obstáculos que impedem a simplificação e aceleração dos procedimentos de
				licenciamento dos projetos de produção de energia renovável (centralizado e
				descentralizado);
			</li>
			<li>
				Quais as medidas a adotar para remoção dos obstáculos/entraves nos licenciamentos elétrico,
				ambiental e urbanístico dos projetos de produção de energia renovável (centralizado e
				descentralizado).
			</li>
		</ol>
		<p>Procedeu-se à reflexão e discussão, de uma forma ampla, dos seguintes pontos:</p>
		<ol>
			<li>
				<b>Identificação de Obstáculos ao Licenciamento ágil e previsível:</b>
				<p>
					Foram identificados como principais obstáculos ao licenciamento de projetos renováveis, a
					complexidade legislativa, falta de articulação entre entidades, insuficiência de recursos
					técnicos e humanos, e desafios de aceitação social e territorial.
				</p>
				<p>Especificamente:</p>
				<ul>
					<li>
						<b>Complexidade Legislativa:</b> Foi referido que a legislação se tem tornado cada vez mais
						complexa, com múltiplos regimes jurídicos e alterações sucessivas que, em vez de simplificar,
						dificultam a implementação dos projetos, criando insegurança e bloqueios administrativos.
					</li>
					<li>
						<b>Falta de Articulação e Recursos:</b> Os participantes apontaram a falta de articulação
						entre entidades centrais, regionais e locais, bem como a escassez de recursos técnicos e
						humanos, especialmente em câmaras municipais, como fatores que atrasam e complicam o licenciamento.
					</li>
					<li>
						<b>Desafios Operacionais e de Rede:</b> Foi destacado que o desenvolvimento insuficiente
						das redes elétricas e a existência de múltiplos licenciamentos parcelares, incluindo questões
						logísticas como licenças/autorizações para a passagem de infraestruturas e linhas, podem
						paralisar projetos de interesse público.
					</li>
					<li>
						<b>Aceitação Social e Territorial:</b> A oposição de Municípios e populações locais, bem
						como a necessidade de negociar contrapartidas sociais e ambientais, foram identificadas como
						obstáculos frequentes, exigindo maior envolvimento e comunicação com os Municípios e as comunidades.
					</li>
					<li>
						<b>Falta de Digitalização:</b> A ausência de plataformas digitais integradas e interoperáveis
						para submissão e acompanhamento de processos foi apontada como um entrave à transparência
						e eficiência do licenciamento.
					</li>
				</ul>
			</li>
			<li>
				<b>Medidas proposta para simplificação e aceleração do licenciamento:</b>
				<p>
					Foram apresentadas como propostas para simplificar e acelerar o processo de licenciamento
					a criação de uma unidade de aceleração, reforço da digitalização, clarificação de regras,
					definição de matrizes de minimização e compensação ambiental.
				</p>
				<ul>
					<li>
						<b>Unidade de Aceleração:</b> Foi sugerida a criação de uma unidade física centralizada para
						gerir e agilizar os processos de licenciamento, reforçando a capacidade das entidades envolvidas
						e apoiando Municípios com menos recursos técnicos.
					</li>
					<li>
						<b>Digitalização e Interoperabilidade:</b> Os participantes defenderam a implementação de
						plataformas digitais integradas para submissão, acompanhamento e partilha de informação sobre
						projetos, facilitando a transparência e a comunicação entre entidades.
					</li>
					<li>
						<b>Clarificação e Uniformização de Regras:</b> Foi proposta a clarificação de zonas cinzentas
						na legislação e a uniformização de critérios de decisão, para evitar interpretações divergentes
						e acelerar a tramitação dos processos.
					</li>
					<li>
						<b>Matrizes de Minimização e Compensação:</b> Discutiu-se a necessidade de definir matrizes
						claras de medidas de minimização e compensação ambiental, adaptadas a cada tipo de projeto
						e contexto, e a importância de monitorização contínua para ajustar medidas conforme necessário.
					</li>
				</ul>
				<p>Adicionalmente debateu-se acerca das ZAER:</p>
			</li>
			<li>
				<b>Vantagens, riscos e limitações das áreas de aceleração para energias renováveis:</b>
				<p>
					Foram abordados critérios de seleção, viabilidade técnica e económica, e potenciais
					desigualdades entre projetos dentro e fora destas zonas. A saber:
				</p>
				<ul>
					<li>
						<b>Critérios de Seleção das Áreas:</b> Foi debatido que os critérios para definição das zonas
						AE tendem a privilegiar fatores ambientais e territoriais, podendo não garantir a viabilidade
						técnica ou económica dos projetos, especialmente para energia eólica, que depende de dados
						específicos de vento.
					</li>
					<li>
						<b>Riscos de Desigualdade:</b> Os participantes alertaram para o risco de criar desigualdades
						entre projetos dentro e fora das zonas AE, sendo provável que muitos projetos continuem a
						ser desenvolvidos fora destas áreas, o que pode gerar obstáculos adicionais e concorrência
						desleal.
					</li>
					<li>
						<b>Contratação de Terrenos:</b> Foi sublinhada a complexidade da contratação de terrenos,
						frequentemente desconsiderada pelas entidades licenciadoras, e que pode obrigar a alterações
						de layout dos projetos, dificultando a sua execução.
					</li>
					<li>
						<b>Impacto nas Comunidades e Municípios:</b> Discutiu-se o papel dos Municípios, que podem
						restringir projetos renováveis a zonas específicas, excluindo áreas agrícolas ou naturais,
						e a necessidade de mecanismos de compensação social e económica para as comunidades afetadas.
					</li>
					<li>
						<b>Monitorização e Informação:</b> Foi sugerida a centralização e partilha de dados de monitorização
						ambiental, atualmente dispersos entre promotores e entidades, para apoiar decisões mais informadas
						e reduzir incertezas.
					</li>
				</ul>
				<p>No âmbito dos trabalhos do focus group foram, ainda, debatidas:</p>
			</li>
			<li>
				<b>
					Questões Técnicas e Ambientais relacionadas com a Avaliação de Impacte Ambiental (AIA):
				</b>
				<p>
					Foram discutidas questões técnicas relacionadas com a monitorização, compensação e
					minimização de impactes com destaque da necessidade e importância de informação detalhada
					e de processos adaptada à realidade dos projetos. Especificamente:
				</p>
				<ul>
					<li>
						<b>Monitorização Ambiental:</b> Foi debatida a necessidade de monitorização contínua, tanto
						prévia como pós-implementação, para identificar impactos não previstos e ajustar medidas
						de mitigação ou compensação, com destaque para a partilha de dados entre promotores e entidades
						concluindo que a monitorização prévia não deveria ser um requisito, considerando o objetivo
						principal de aceleração. Foi enfatizada a importância da monitorização pós-implementação
						para, conforme a RED III, poderem tomar-se medidas suplementares caso fosse detetado algum
						impacto não esperado. Deu-se também nota da possibilidade de realização de um "screening"
						e/ou, embora distinto do screening, uma AInCa uma vez que esta pode ser usada em zonas fora
						da Rede Natura. A ausência de AIA nas ZAER poderá ser encarada com preocupação principalmente
						pela necessidade de preservação de valores naturais relevantes fora da Rede Natura.
					</li>
					<li>
						<b>Minimização vs. Compensação:</b> Os participantes defenderam que a prioridade deve ser
						dada a medidas de minimização dos impactos ambientais, recorrendo à compensação apenas quando
						estritamente necessário, e que as matrizes de medidas devem ser claras e adaptáveis. As compensações
						podem não ser apenas económicas e incluir monitorização. Referiu-se também o melhoramento
						de pastagens como eventual medida de compensação.
					</li>
					<li>
						<b>Desafios de Informação:</b> Foi identificada a falta de informação detalhada e centralizada
						sobre valores naturais e habitats, dificultando a avaliação de impactos e a definição de
						medidas adequadas, sendo sugerida a criação de bases de dados georreferenciadas. Só havendo
						um cadastro dos valores naturais é possível reduzir a incerteza. Foi notada a enorme quantidade
						de dados de monitorização que já existem mas que não estão centralizados nem prontos a utilizar
						para apoiar as AIA.
					</li>
					<li>
						<b>Particularidades das Áreas Artificializadas:</b> Discutiu-se que, nas áreas artificializadas,
						o principal obstáculo é a falta de capacidade técnica e de meios para licenciamento, e que
						processos simplificados podem ser aplicados a projetos de menor dimensão.
					</li>
					<li>
						<b>Seleção de ZAER:</b> as regras relativas à biodiversidade têm de ser muito precaucionárias
						devido às dinâmicas próprias da biodiversidade e devem ser distintas para solar e para eólica.
						Considera que as medidas de minimização devem ser o mais concretas possíveis (guião/checklist
						à qual os projetos têm de obedecer). Os eventuais mecanismos de compensação devem ser no
						local/região do projeto. Referiu-se o direito europeu sobre conservação de algumas espécies
						que inibe as entidades. Foi também notada a existência de legislação específica para o sobreiro
						e azinheira que deve ser tida em conta. Mais se notou a necessidade de se evitar a institucionalização
						de critérios que depois possam ser usados para bloquear outros projetos; que os projetos
						que estão isentos de AIA têm, atualmente, que pedir pareceres a inúmeras entidades, o que,
						na prática, complexifica o processo e adia a execução propriamente dita; as dificuldades
						ao nível dos licenciamentos dos projetos de Repowering, cujos impactos não podem ser avaliados
						com base na monitorização, uma vez que a substituição das turbinas acarreta impactos diversos.
					</li>
					<li>
						<b>Planeamento, Comunicação e Envolvimento das Partes Interessadas:</b> enfatizaram a necessidade
						de um planeamento estratégico, comunicação clara e envolvimento de todas as partes interessadas,
						incluindo promotores, Municípios e comunidades, para garantir o sucesso do licenciamento
						ágil e previsível e a aceitação social dos projetos.
					</li>
					<li>
						<b>Planeamento Estratégico:</b> Foi sublinhada a importância de um planeamento setorial robusto,
						com definição clara de áreas potenciais para renováveis, evitando ambiguidades e assegurando
						a compatibilidade com metas nacionais e europeias.
					</li>
					<li>
						<b>Comunicação e Transparência:</b> Os participantes destacaram a necessidade de melhorar
						a comunicação pública sobre o que são as áreas de aceleração, evitando mal-entendidos e promovendo
						a aceitação social dos projetos.
					</li>
					<li>
						<b>Envolvimento dos Municípios e Comunidades:</b> Foi defendido o envolvimento ativo dos
						Municípios e das comunidades locais no processo de planeamento e licenciamento, garantindo
						que as suas preocupações e interesses sejam considerados e que beneficiem dos projetos.
					</li>
					<li>
						<b>Mudança de Paradigma:</b> apelou-se a uma mudança radical de práticas, incentivando os
						participantes a pensar em soluções inovadoras e a contribuir para um novo modelo de licenciamento
						que seja verdadeiramente ágil, previsível e eficaz.
					</li>
				</ul>
			</li>
		</ol>

		<h2>Grupo Focal SISTEMA ELÉTRICO NACIONAL</h2>
		<p><b>Coordenação:</b> Professor Pedro Carvalho</p>
		<p>
			<b>Objetivos e âmbito:</b> os principais desafios da integração dos projetos de energia solar e
			eólica no sistema elétrico, com especial atenção aos impactos na rede, com o intuito de identificar
			estratégias para a definição das zonas de aceleração que permitam mitigar os efeitos das limitações
			das redes no curto-prazo.
		</p>
		<p>
			<b>Especialistas:</b> Rui Bento, Patrícia Fortes, Ana Estanqueiro, Hugo Costa, Rita Mota, Rui Oliveira
		</p>
		<p><b>Reuniões realizadas:</b> 1</p>
		<h3>Reunião nº1</h3>
		<h4>Principais pontos discutidos:</h4>
		<p>
			O Focus Group identificou que o principal obstáculo à concretização das metas do PNEC 2030 não
			é a falta de recursos renováveis, mas sim as limitações estruturais, regulatórias e
			operacionais do Sistema Elétrico Nacional (SEN). Sem ajustes rápidos, existe um risco elevado
			de as metas se manterem no plano estratégico, sem tradução em capacidade instalada efetiva.
		</p>
		<ol>
			<li>
				<b>Rede elétrica como gargalo crítico</b>
				<p>
					O acesso à rede, sobretudo à rede de transporte, é a barreira dominante à expansão das
					renováveis. A existência de cerca de 14.000 MVA de capacidade comprometida e não
					utilizada, aliada a regras rígidas de reforços e repartição de custos, gera incerteza,
					bloqueia projetos maduros e penaliza de forma desproporcionada os projetos de menor
					escala.
				</p>
				<p>
					A atribuição de ligações com restrições seria benéfica, mas o risco não pode ficar no
					operador de rede (o risco da parcela de consumo que dá suporte à capacidade de injeção
					desaparecer, reduzindo, nesses momentos, a capacidade de injeção).
				</p>
			</li>
			<li>
				<b>2. Desalinhamento entre planeamento, metas e economia</b>
				<p>As metas do PNEC não estão suficientemente alinhadas com:</p>
				<ul>
					<li>a capacidade real da rede,</li>
					<li>o planeamento do RMSA,</li>
					<li>a viabilidade económica dos projetos.</li>
				</ul>
				<p>
					Algumas metas (solar e offshore) são vistas como excessivamente otimistas, enquanto PPAs
					muito baixos comprometem a execução efetiva da capacidade atribuída.
				</p>
			</li>
			<li>
				<b>Zonas de aceleração: oportunidade com riscos</b>
				<p>
					As zonas de aceleração podem reduzir prazos de licenciamento, mas não devem ser
					interpretadas como zonas exclusivas. Para serem eficazes, devem:
				</p>
				<ul>
					<li>partir da rede existente e planeada,</li>
					<li>
						integrar parques eólicos existentes (repotenciação, sobreequipamento, hibridização),
					</li>
					<li>aproximar produção da procura (zonas industriais e autoconsumo).</li>
				</ul>
				<p>Caso contrário, correm o risco de ser pouco relevantes para as metas até 2030.</p>
			</li>
			<li>
				<b>4. Modelo ineficiente de atribuição de capacidade</b>
				<p>
					O modelo atual ("first come, first served") gera concentrações inviáveis, filas bloqueadas
					e especulação. Defende-se:
				</p>
				<ul>
					<li>atribuição de capacidade por zonas com limites claros,</li>
					<li>milestones obrigatórios,</li>
					<li>regras eficazes de caducidade e libertação de capacidade.</li>
				</ul>
			</li>
			<li>
				<b>Licenciamento ambiental como fator temporal crítico</b>
				<p>
					O licenciamento ambiental é um dos principais entraves, com processos que podem
					ultrapassar 4 anos. As zonas de aceleração podem ajudar, mas o impacto até 2030 depende do
					calendário e da integração de projetos já em desenvolvimento. Repotenciação e hibridização
					deveriam ter prioridade.
				</p>
			</li>
			<li>
				<b>Subaproveitamento da rede existente</b>
				<p>
					Existe capacidade física bloqueada por critérios conservadores e "entendimentos de papel".
					Foram identificados "quick wins" até 2030:
				</p>
				<ul>
					<li>Dynamic Line Rating,</li>
					<li>flexibilização transitória do critério N-1,</li>
					<li>uso de transformadores de reserva,</li>
					<li>revisão da interface transporte–distribuição.</li>
				</ul>
			</li>
			<li>
				<b>Repotenciação, hibridização, armazenamento e bombagem</b>
				<p>
					A repotenciação eólica, as centrais híbridas e o armazenamento são soluções custo-eficazes
					para aumentar renováveis sem grandes reforços de rede. Contudo, persistem barreiras
					regulatórias, contratuais e de remuneração da flexibilidade. A bombagem hidroelétrica é
					reconhecida como essencial, mas está bloqueada por interpretações ambientais e
					enquadramentos legais desajustados.
				</p>
			</li>
			<li>
				<b>Autoconsumo, agregadores e digitalização</b>
				<p>
					O autoconsumo e a proximidade entre produção e consumo são vistos como grandes
					aceleradores. Falta um regime claro de associação dinâmica injeção–consumo. Agregadores,
					centrais virtuais e maior digitalização da rede são cruciais para libertar capacidade e
					otimizar o SEN, mas o enquadramento regulatório ainda é insuficiente.
				</p>
			</li>
		</ol>
