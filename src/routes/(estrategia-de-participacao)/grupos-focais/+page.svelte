		<h3>Reunião nº1</h3>
		<h4>Principais pontos discutidos:</h4>
		<p>
			Foram apresentadas as 3 questões que orientaram esta primeira reunião, dando-se a liberdade
			aos intervenientes de comentarem qualquer delas:
		</p>
		<ul>
			<li>Qual a importância e o potencial do agrovoltaico?</li>
			<li>Quais as limitações técnicas, administrativas ou políticas?</li>
			<li>Em que tipo de áreas agrícolas existentes e que tipo de agricultura a desenvolver.</li>
		</ul>
		<h4>Notas da reunião:</h4>
		<ul>
			<li>
				<b>Definição e Enquadramento dos Sistemas AgroVoltaicos:</b>
				<p>
					Foi debatido o conceito de agrovoltaico, as diferenças face a outros modelos
					(eco-voltaico), a ausência de definição legal clara em Portugal e a necessidade de
					clarificação para efeitos de políticas públicas e licenciamento.
				</p>
				<ul>
					<li>
						<b>Definição do Conceito:</b> Foi debatida a ausência de uma definição legal e técnica clara
						para agrovoltaico em Portugal, sendo referidas diferentes abordagens internacionais: nos
						EUA, qualquer integração de fotovoltaico em contexto agrícola é considerada agrovoltaico,
						enquanto na Europa exige-se adaptação da instalação fotovoltaica à atividade agrícola, como
						aumento de altura dos painéis ou espaçamento entre linhas para permitir mecanização e produção
						agrícola.
					</li>
					<li>
						<b>Diferenças entre Modelos:</b> Os participantes distinguiram entre agrovoltaico (foco na
						compatibilização entre produção agrícola e energia) e eco-voltaico (abordagem mais abrangente,
						incluindo benefícios ambientais e sociais), sendo sugerido que o conceito eco-voltaico pode
						englobar o agrovoltaico e outros usos híbridos do solo.
					</li>
					<li>
						<b>Necessidade de Definição Legal:</b> Foi consensual a necessidade de uma definição legal
						clara para evitar apropriação indevida do conceito e garantir que políticas de apoio e licenciamento
						sejam direcionadas para projetos que efetivamente conciliem agricultura e produção de energia,
						evitando que projetos puramente fotovoltaicos sejam classificados como agrovoltaicos sem
						adaptação à atividade agrícola.
					</li>
					<li>
						<b>Exemplos Internacionais:</b> Foram referidas experiências em França, Itália, Alemanha,
						EUA, Brasil e China, destacando diferentes percentagens de ocupação do solo, tipos de integração
						e objetivos (produção, biodiversidade, resiliência), ilustrando a diversidade de abordagens
						e a necessidade de Portugal definir o seu próprio enquadramento.
					</li>
				</ul>
			</li>
			<li>
				<b>Potencial, Benefícios e Limitações dos Sistemas AgroVoltaicos:</b>
				<p>
					Os especialistas analisaram o potencial dos sistemas agrovoltaicos para a produção de
					energia e agricultura, os benefícios ambientais e sociais, e as limitações técnicas,
					económicas e legais para a sua implementação em Portugal.
				</p>
				<ul>
					<li>
						<b>Benefícios Agrícolas e Ambientais:</b> Foi destacado o potencial dos sistemas agrovoltaicos
						para proteger culturas agrícolas (como vinha e olival) contra eventos climáticos extremos,
						reduzir o consumo de água devido ao sombreamento dos painéis, promover a biodiversidade e
						contribuir para a resiliência das comunidades rurais, especialmente em territórios agrícolas
						de menor valor. Mas ainda há pouca informação sobre o potencial impacto na produtividade
						do olival. Foi contrastada a diferença entre o potencial do agrovoltaico como agregador de
						comunidades, contrariamente ao solar puro. Foi igualmente mencionada a sua potencial importância
						na proteção do vento a na redução do risco de incêndio. Na componente biodiversidade foi
						mencionado que o potencial benefício dependerá muito do contexto, com preferência para a
						instalação em sistemas agrícolas mais intensivos com menor potencial de biodiversidade.
					</li>
					<li>
						<b>Limitações Técnicas e Operacionais:</b> Foram identificadas limitações técnicas, como
						a necessidade de adaptar a altura e espaçamento dos painéis para permitir mecanização, desafios
						na compactação e qualidade do solo, e dificuldades de compatibilização com culturas superintensivas
						(ex: olival e vinha), onde as máquinas de colheita são de grandes dimensões e exigem adaptações
						tecnológicas ainda inexistentes em Portugal. Mas foi igualmente mencionado o potencial das
						estufas para terem este tipo de sistema. Foram apontadas dificuldades de interoperabilidade
						tecnológica, falta de soluções adaptadas ao contexto agrícola, carências de infraestruturas
						elétricas e de comunicações em meio rural, e necessidade de maior literacia cruzada entre
						especialistas dos setores agrícola e energético.
					</li>
					<li>
						<b>Modelos de Negócio e Complexidade:</b> A adoção de sistemas agrovoltaicos implica novos
						modelos de negócio, que podem envolver o agricultor como proprietário, arrendatário ou em
						regime de copropriedade, aumentando a complexidade da gestão e exigindo parcerias entre os
						setores agrícola e energético.
					</li>
					<li>
						<b>Limitações Legais e de Licenciamento:</b> Foi referido que o quadro legal e regulatório
						português está desatualizado para o contexto agrovoltaico, com obstáculos burocráticos ao
						licenciamento, restrições à venda de excedentes à rede e limitações específicas para instalação
						em áreas da Reserva Agrícola Nacional (RAN), REN, e outros solos protegidos. Os principais
						desafios ao licenciamento e implementação dos sistemas agrovoltaicos incluem questões técnicas,
						operacionais, económicas, comportamentais e legais, e foram sugeridos possíveis caminhos
						para simplificação e aceleração do processo. O licenciamento de projetos de autoconsumo enfrenta
						entraves burocráticos, com o setor agrícola frequentemente impedido de vender excedentes
						à rede devido a incentivos recebidos, e um quadro legal desatualizado que não contempla as
						especificidades dos sistemas agrovoltaicos.
					</li>
					<li>
						<b>Viabilidade Económica:</b> Discutiu-se que os custos de instalação dos sistemas agrovoltaicos
						são superiores aos dos parques fotovoltaicos convencionais devido à menor densidade de painéis
						e necessidade de estruturas elevadas, mas que podem ser compensados por benefícios adicionais
						e, em alguns casos, por melhores business plans devido à produção de energia em horários
						de maior valor. A complexidade dos modelos de negócio, a necessidade de clarificação de incentivos
						e a adaptação das políticas de apoio foram identificadas como fatores críticos para a penetração
						da tecnologia, sendo sugerida a simplificação do licenciamento para o setor agrícola em vez
						de uma abordagem baseada apenas em concentração espacial.
					</li>
				</ul>
			</li>
			<li>
				<b>
					Critérios para Identificação de Áreas de Aceleração e Compatibilização com Sistemas
					AgroVoltaicos:
				</b>
				<p>
					Foram debatidos os critérios para seleção de áreas de aceleração para energias renováveis,
					a compatibilização com diferentes sistemas agrícolas (incluindo RAN, olivais, vinhas e
					pomares) e as implicações legais e ambientais dessas escolhas. Foi questionado se deveria
					haver uma ZAER para agrovoltaicos.
				</p>
				<ul>
					<li>
						<b>Critérios de Seleção de Áreas:</b> Alguns participantes acham que as áreas de aceleração
						devem privilegiar solos agrícolas de menor valor e evitar solos férteis e produtivos, tendo
						em conta a proteção do solo e da paisagem, bem como a legislação que limita a ocupação de
						áreas RAN e REN a 10% da área total contratada ou 1 hectare. Em contraste, outros receiam
						o impacto sobre a biodiversidade, porque são estas áreas como menor valor agrícola que possuem
						muitas vezes maior potencial para biodiversidade.
					</li>
					<li>
						<b>Compatibilização com Sistemas Agrícolas:</b> Os participantes debateram a viabilidade
						de implementar agrovoltaicos em diferentes sistemas agrícolas, concluindo que pastagens e
						culturas extensivas são mais facilmente compatibilizáveis, enquanto olivais e vinhas superintensivos
						apresentam maiores desafios técnicos e económicos, sendo prioritário aprofundar a investigação
						nestes casos.
					</li>
					<li>
						<b>Implicações Legais:</b> Foi esclarecido que a legislação atual impõe restrições à instalação
						de sistemas agrovoltaicos em áreas protegidas. De qualquer foram, estas estariam excluídas
						das ZAER.
					</li>
					<li>
						<b>Impactos Ambientais e Sociais:</b> Salientou-se a importância de considerar os impactos
						ambientais, nomeadamente a compactação e degradação do solo, e os impactos sociais, como
						a aceitação pelas comunidades locais, defendendo que a abordagem deve ser descentralizada
						e adaptada ao contexto territorial.
					</li>
					<li>
						<b>Propostas de Melhoria:</b> Foi sugerida a criação de standards e boas práticas ambientais
						para parques fotovoltaicos em solo rústico, a clarificação do conceito de agrovoltaico para
						efeitos de licenciamento e incentivos, e a promoção de parcerias entre agricultores e promotores
						energéticos para garantir benefícios mútuos e aceitação social.
					</li>
				</ul>
			</li>
			<li>
				<b>Próximos Passos e Organização do Processo:</b>
				<p>
