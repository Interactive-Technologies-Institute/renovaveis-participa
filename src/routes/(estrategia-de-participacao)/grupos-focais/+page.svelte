		<h2>Grupo Focal SOLUÇÕES URBANAS</h2>
		<p><b>Coordenação:</b> Dr. Sérgio Barroso e Professor Pedro Carvalho</p>
		<p>
			<b>Objetivos e âmbito:</b> como acelerar a produção de energias renováveis em áreas urbanas e que
			critérios devem orientar o desenvolvimento dessas soluções, maximizando benefícios económicos,
			ambientais e sociais, e mitigando impactos?
		</p>
		<p>
			<b>Especialistas:</b> Ana Rita Antunes, Rui Bento, Manuel Miranda, Miguel Sequeira
		</p>
		<p><b>Reuniões realizadas:</b> 1</p>
		<h3>Reunião nº1</h3>
		<h4>Principais pontos discutidos:</h4>
		<ol>
			<li>
				<b>Potencial Técnico do Fotovoltaico Urbano e Limitações do Edificado</b>
				<ol>
					<li>
						<b>Potencial de fotovoltaico em coberturas urbanas</b>
						<p><b>Conclusão:</b></p>
						<p>
							Os estudos existentes apontam para um potencial técnico elevado em coberturas,
							incluindo um valor de referência de 28 GW no estudo do LNEG. No entanto, a
							exequibilidade prática a curto/médio prazo é duvidosa devido:
						</p>
						<ul>
							<li>
								Qualidade estrutural deficiente de muitos telhados e necessidade de obras prévias;
							</li>
							<li>Sombreamentos (chaminés, antenas, volumes técnicos);</li>
							<li>Ausência de dados detalhados sobre o estado do edificado.</li>
						</ul>
						<p>
							Ainda assim, reconhece‑se que o potencial teórico existe e que a reabilitação do
							edificado em mau estado é desejável e pode criar oportunidade para integração de
							renováveis, embora implique "dois investimentos".
						</p>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								O estudo do LNEG indica 28 GW de potencial em coberturas; há dúvidas quanto à sua
								concretização.
							</li>
							<li>
								Informação da maior associação de proprietários de Lisboa: cerca de metade dos
								telhados não é aproveitável no estado atual, necessitando de obras prévias.
							</li>
							<li>
								O estudo nacional não avaliou o efeito de chaminés/volumes técnicos nem a condição
								estrutural cobertura a cobertura; usou percentagens de aproveitamento conservadoras,
								baseadas em trabalhos detalhados apenas para Lisboa (Lisboa ENova).
							</li>
							<li>Não existem dados suficientemente granulares sobre a qualidade do edificado.</li>
							<li>
								Argumenta‑se que telhados em mau estado devem ser reabilitados de qualquer forma,
								podendo a instalação de solar fotovoltaico ser integrada, ainda que com custos
								acrescidos.
							</li>
						</ul>
					</li>
				</ol>
			</li>
			<li>
				<b>Autoconsumo Urbano, Modelos Económicos e Viabilidade Financeira</b>
				<ol>
					<li>
						<b>Autoconsumo fotovoltaico urbano: potencial, estrangulamentos e modelos</b>
						<p><b>Conclusão:</b></p>
						<p>
							O autoconsumo fotovoltaico em meio urbano apresenta potencial técnico relevante e
							forte motivação económica do lado dos consumidores (poupança na fatura), mas enfrenta
							vários estrangulamentos:
						</p>
						<ul>
							<li>
								Redução da atratividade económica face a contratos dinâmicos e aos preços de mercado
								em horas solares;
							</li>
							<li>
								Estrutura tarifária (períodos, horários, tarifas de acesso) que desincentiva o
								investimento;
							</li>
							<li>Complexidade regulatória e tecnológica do autoconsumo coletivo;</li>
							<li>
								Falta de modelos económicos adicionais (comunidades de energia, feed‑in tariffs) que
								valorizem excedentes e viabilizem projetos.
							</li>
						</ul>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								Motivação económica principal é a poupança; o setor residencial/social tem pouco
								capital e menor capacidade para "fazer contas".
							</li>
							<li>
								O autoconsumo individual torna‑se menos atrativo face a contratos dinâmicos; a
								revisão tarifária em curso pode reduzir ainda mais a viabilidade da Unidade de
								Produção para Autoconsumo (UPAC) urbano.
							</li>
							<li>
								Necessidade de novos modelos para além do autoconsumo individual: Comunidades de
								energia, autoconsumo coletivo, VNM, feed‑in tariffs urbanos.
							</li>
							<li>
								Exemplos: feed‑in tariffs para pequena produção (Holanda) ou para energia partilhada
								em comunidade (Itália).
							</li>
							<li>
								O excedente do autoconsumo coletivo não é devidamente valorizado; o regime é
								complexo e comparativamente menos simples que em Itália e Espanha.
							</li>
							<li>
								Há estrangulamentos tecnológicos e de desenho (proximidade, papel do gestor de
								energia, etc.) que dificultam implementação.
							</li>
							<li>
								O potencial descentralizado é elevado e o crescimento centralizado vs.
								descentralizado tem sido comparável; apoios do Fundo Ambiental deram um forte
								impulso recente.
							</li>
							<li>
								As metas do PNEC 2030 favorecem o centralizado face ao descentralizado, apesar de
								ser duvidoso que o centralizado atinja os valores previstos até 2030.
							</li>
							<li>
								A queda de preços de mercado afeta mais o centralizado em mercado do que o
								autoconsumo residencial, cujo referencial é o valor da fatura.
							</li>
						</ul>
					</li>
					<li>
						<b>Modelos de autoconsumo urbano: individual, coletivo e comunidades</b>
						<p><b>Conclusão:</b></p>
						<p>
							O autoconsumo coletivo e as comunidades de energia são essenciais para maximizar o
							aproveitamento das coberturas urbanas, dado que o autoconsumo individual é
							frequentemente pouco rentável em edifícios com baixa taxa de consumo diurno. Contudo,
							barreiras regulatórias (nomeadamente tarifárias) reduzem a atratividade económica
							destes modelos.
						</p>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								Em muitos equipamentos (pavilhões, edifícios públicos) a entidade proprietária
								consome apenas uma pequena fração da produção; modelos coletivos permitem
								rentabilizar a cobertura.
							</li>
							<li>
								Condomínios urbanos têm telhados limitados para o conjunto dos condóminos; o modelo
								coletivo é essencial.
							</li>
							<li>
								Questiona‑se o destino e a remuneração dos excedentes, bem como a viabilidade de
								modelos de "renda de telhado" por empresas investidoras.
							</li>
							<li>
								Em Portugal, paga‑se tarifa de acesso à rede na partilha local, ao contrário de
								Itália e Espanha, constituindo um forte desincentivo e uma fonte de incerteza anual.
							</li>
							<li>
								Experiências municipais (ex.: Sintra) estudam a produção fora de áreas históricas
								para abastecer consumidores dentro, dependente do regime de partilha.
							</li>
						</ul>
					</li>
					<li>
						<b>Valor, benefícios e aceitação social das comunidades de energia</b>
						<p><b>Conclusão:</b></p>
						<p>
							As comunidades de energia podem oferecer benefícios significativos de aceitação
							social, otimização do uso da energia, descarbonização e criação de valor coletivo,
							sendo potencialmente mais valiosas do que a soma dos autoconsumos individuais, se
							existir enquadramento técnico, regulatório e financeiro adequado.
						</p>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								O incentivo concreto para criar comunidades, face à simplicidade do autoconsumo
								individual, precisa de ser clarificado.
							</li>
							<li>
								Comunidades permitem melhor aproveitamento da produção local, ao partilhar energia
								entre OS vários perfis de consumo.
							</li>
							<li>
								Há modelos que vão além da produção, incorporando mobilidade elétrica partilhada
								(automóveis e pontos de carregamento partilhados).
							</li>
							<li>
								O interesse social existe e é maior do que o esperado (número de candidaturas a
								projetos europeus excede expectativas).
							</li>
							<li>
								Experiências internacionais (Holanda, Bélgica) mostram crescimento rápido do número
								de comunidades e critérios de seleção que valorizam benefícios sociais e ambientais.
							</li>
						</ul>
					</li>
				</ol>
			</li>
			<li>
				<b>Matriz Energética, Rede, Fontes Renováveis e Segurança do Sistema</b>
				<ol>
					<li>
						<b>Papel das diferentes fontes renováveis e impactos sistémicos</b>
						<p><b>Conclusão:</b></p>
						<p>
							A atual abordagem está excessivamente centrada no fotovoltaico, em particular no
							centralizado. O solar fotovoltaico tem contributo limitado no inverno e pode ocupar a
							capacidade de rede necessária a fontes com maior fator de utilização (eólico, hídrico,
							biomassa). É necessário diversificar a matriz, apostar em hibridização (eólico+solar
							fotovoltaico, hídrico+solar fotovoltaico), explorar solar térmico e outras
							tecnologias, e aproveitar melhor o solar fotovoltaico em combinação com
							infraestruturas existentes, ajustando consumos aos períodos de maior produção solar.
						</p>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								Existem já níveis de potência de solar fotovoltaico comparáveis à ponta do sistema;
								continuar a crescer pode reduzir espaço de rede para fontes relevantes no inverno.
							</li>
							<li>
								Eólico apresenta fatores de utilização mais elevados e produz mais no inverno, sendo
								crucial para segurança de abastecimento.
							</li>
							<li>
								Hibridizar parques eólicos e centrais hídricas com solar fotovoltaico é uma
								estratégia virtuosa, aproveitando as infraestruturas existentes e melhorando
								rendimento da rede.
							</li>
							<li>
								solar fotovoltaico urbano é virtuoso para envolver agentes e sincronizar consumos,
								mas o seu contributo para a descarbonização global é limitado em termos absolutos.
							</li>
							<li>
								A rede, até certa escala (cerca de 1 kVA), não é o principal entrave ao autoconsumo,
								mas surgem limites com projetos maiores e novas grandes cargas (eletrolisadores).
							</li>
							<li>
								Pedidos de ligação de eletrolisadores (ordem de grandeza 3x a ponta atual) podem
								alterar profundamente o sistema, exigindo abordagem específica.
							</li>
						</ul>
					</li>
					<li>
						<b>Metas PNEC, projetos centralizados (8 GW) e papel do descentralizado</b>
						<p><b>Conclusão:</b></p>
						<p>
							É plausível que seja mais fácil cumprir as metas do PNEC 2030 na componente
							descentralizada (autoconsumo e comunidades de energia) do que através da concretização
							integral do pipeline de grandes projetos centralizados (~8 GW com TRC/acordos de
							ligação), desde que existam instrumentos financeiros adequados e remoção de entraves
							administrativos.
						</p>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								Existem cerca de 8 GW com TRC ou acordos de ligação; é questionado se não será mais
								simples atingir metas via descentralizado.
							</li>
							<li>
								Apoios do Fundo Ambiental demonstraram capacidade de gerar 1–2 GW adicionais em
								poucos anos no segmento descentralizado.
							</li>
							<li>
								O autoconsumo doméstico tem limites de poupança (baixa presença em casa durante o
								dia), mas é relevante para a transição e para deslocar consumos.
							</li>
							<li>
								O solar fotovoltaico descentralizado é mais caro (CAPEX) do que o centralizado,
								implicando políticas de apoio específicas se se quiser privilegiar esta via.
							</li>
						</ul>
					</li>
				</ol>
			</li>
			<li>
				<b>Desenho Tarifário, Regulação Económica e Sinais de Mercado</b>
				<ol>
					<li>
						<b>Desenho tarifário, tarifas de acesso e incentivos</b>
						<p><b>Conclusão:</b></p>
						<p>
							A estrutura tarifária atual, baseada em custos sobretudo fixos cobrados via componente
							variável (kWh), distorce incentivos:
						</p>
						<ul>
							<li>Reduz o incentivo ao autoconsumo e à produção urbana;</li>
							<li>Dificulta deslocar consumos para períodos de elevada produção solar;</li>
							<li>Não reflete a natureza dos custos do sistema;</li>
							<li>
								Há movimento regulatório para aumentar componentes fixas e rever períodos horários,
								mas há risco de estas alterações irem contra objetivos de promoção do solar
								fotovoltaico urbano e da eficiência se não forem bem calibradas.
							</li>
						</ul>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								O sistema tem custos principalmente fixos (infraestruturas e centrais) e custos
								variáveis hoje marginais, mas cobra‑se maioritariamente por kWh.
							</li>
							<li>
								Propõe‑se reequilibrar as componentes fixas e variáveis, aumentando o custo fixo
								indexado à potência contratada e reduzindo o custo variável, sem eliminar incentivo
								à eficiência.
							</li>
							<li>
								A ERSE já caminha para aumentar a componente fixa da potência e rever tarifas de
								acesso, mas a sua margem abrange apenas essa componente.
							</li>
							<li>
								A revisão dos períodos horários (retirar ponta de manhã, concentrar à noite) pode
								prejudicar o incentivo a FV urbano e à deslocação de consumos para o dia.
							</li>
							<li>
								As tarifas deveriam favorecer o consumo diurno no verão (vazio diurno) para
								aproveitar excedentes solares; hoje ainda incentivam consumo noturno.
							</li>
							<li>
								A adoção de tarifas indexadas é baixa entre potenciais autoconsumidores urbanos,
								limitando o efeito dos sinais de preço horário.
							</li>
							<li>
								Muitos consumidores pagam tarifas elevadas por inércia, mantendo viável o
								autoconsumo, apesar de preços de mercado baixos em certas horas.
							</li>
						</ul>
					</li>
					<li>
						<b>Regime tarifário e enquadramento do autoconsumo coletivo</b>
						<p><b>Conclusão:</b></p>
						<p>
							O regime tarifário e regulatório português para autoconsumo coletivo e comunidades de
							energia é menos favorável e mais complexo do que em Espanha e Itália, estrangulando o
							desenvolvimento de comunidades de energia e dificulta a viabilidade económica de
							projetos.
						</p>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								A tarifa de acesso às redes é considerada elevada, mesmo com isenções parciais
								temporárias, dificultando a partilha económica em condomínios.
							</li>
							<li>
								O licenciamento de autoconsumo individual até 30 kW (a ser alargado a 100 kW) é
								muito simplificado, mas não existe um regime equivalente para o autoconsumo
								coletivo.
							</li>
							<li>
								Em Portugal, exige‑se que o licenciamento seja feito já com Códigos do Ponto de
								Entrega (CPE) de consumo, casados, ao contrário de Itália e Espanha, onde é possível
								licenciar antes de identificar todos os consumidores.
							</li>
							<li>
								Dados públicos sobre Autoconsumo Coletivo e comunidades de energia são escassos e
								pouco transparentes; há cerca de 640 Autoconsumo Coletivo e apenas 3 CER registadas.
							</li>
							<li>
								Condicionantes de rede e critérios de proximidade (2 km por PT) limitam o desenho e
								crescimento das comunidades.
							</li>
						</ul>
					</li>
				</ol>
			</li>
			<li>
				<b>Incentivos Financeiros e Fiscais</b>
				<ol>
					<li>
						<b>Incentivos financeiros e fiscais</b>
						<p><b>Conclusão:</b></p>
						<p>
							Instrumentos financeiros (fundos de apoio) e incentivos fiscais (IVA, IRS,
							eventualmente IRC) são vistos como fundamentais para expandir o autoconsumo e as
							comunidades de energia, sobretudo no segmento urbano. Sem novos apoios, o ritmo de
							crescimento recente dificilmente se manterá, em especial nos domésticos e em parte da
							indústria.
						</p>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								Apoios do Fundo Ambiental foram o principal impulso para o crescimento
								descentralizado; "mais um ou dois fundos" semelhantes poderiam bastar para metas
								PNEC 2030.
							</li>
							<li>
								Redução do IVA (ex. de 23% para 6%) e benefícios em IRS tornariam o investimento
								mais atrativo para famílias e proprietários.
							</li>
							<li>
								Sem estes incentivos, muitos proprietários não avançam, apesar de reconhecerem o
								potencial.
							</li>
							<li>
								Nos domésticos, o crescimento tem sido muito associado à existência de fundos; na
								indústria, muitos casos mais rentáveis já investiram.
							</li>
						</ul>
					</li>
				</ol>
			</li>
			<li>
				<b>Benefícios Urbanos, Equidade, Aceitação Social e Risco de Centralização</b>
				<ol>
					<li>
						<b>Benefícios urbanos, equidade e riscos de "centralização camuflada"</b>
						<p><b>Conclusão:</b></p>
						<p>
							Há preocupação em assegurar que a expansão de renováveis, incluindo soluções
							descentralizadas, gere benefícios efetivos para residentes e utentes urbanos
							(económicos, sociais, de resiliência) e não apenas impactos visuais ou custos.
							Pretende‑se evitar repetir situações em que infraestruturas (como hídricas) trazem
							poucos benefícios locais.
						</p>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								A aceitação social depende de benefícios efetivos e duradouros para as populações,
								não apenas da produção renovável em abstrato.
							</li>
							<li>
								A produção localizada pode reforçar a resiliência energética urbana, mas devem
								evitar‑se modelos que se aproximem de "centralização camuflada".
							</li>
							<li>
								A questão dos excedentes é central: perdas não remuneradas configuram forte
								ineficiência económica.
							</li>
							<li>
								Defende‑se que as áreas urbanas produzam o máximo da sua própria energia,
								especialmente perante novas cargas (data centers, hidrogénio), contribuindo para
								cidades neutras em carbono.
							</li>
						</ul>
					</li>
				</ol>
			</li>
			<li>
				<b>Paisagem, Património, Normativos Urbanísticos e Licenciamento</b>
				<ol>
					<li>
						<b>Impactos paisagísticos, património e regulação urbanística</b>
						<p><b>Conclusão:</b></p>
						<p>
							Os impactos paisagísticos e patrimoniais são relevantes, mas a intensidade atual de
							procura por soluções solares urbanas ainda não gera conflito estrutural com os
							constrangimentos urbanísticos. Defende‑se uma regulação graduada ("áreas do sim, do
							não e do talvez"), com critérios claros, evitando tanto o proibicionismo generalizado
							como a análise casuística pesada para todos os casos.
						</p>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								Distinção entre impacto no elemento individual e no conjunto urbano; necessidade de
								regulação graduada.
							</li>
							<li>
								A lógica de "áreas do sim, não e talvez" (Nuno Portas) é proposta como quadro
								conceptual.
							</li>
							<li>
								Questiona‑se se os mecanismos de salvaguarda existentes não serão já suficientes em
								algumas áreas históricas.
							</li>
							<li>
								É apontada assimetria entre proteção de património cultural urbano e de património
								natural em áreas rústicas (Natura 2000, parques naturais).
							</li>
							<li>
								Exemplos internacionais (Viena, Holanda) mostram integração de solar fotovoltaico em
								património histórico, sugerindo possibilidades também em Portugal, excetuando casos
								de edifícios emblemáticos.
							</li>
							<li>
								Reconhece‑se que a procura atual por soluções solares urbanas continua longe de
								saturar e criar constrangimentos urbanísticos.
							</li>
						</ul>
					</li>
					<li>
						<b>Normativos urbanísticos, licenciamento e "bitola única"</b>
						<p><b>Conclusão:</b></p>
						<p>
							Os normativos urbanísticos são em parte obsoletos, genéricos e baseados numa "bitola
							única" para alterações do aspeto exterior (incluindo painéis solares e bombas de
							calor). Isso gera entraves e decisões pouco racionais, mas os participantes defendem
							uma reforma criteriosa dos mecanismos de controlo, não a sua pura desregulação.
						</p>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								Casos concretos (ex.: retirada obrigatória de bombas de calor em Lisboa) ilustram
								conflito entre normativos atuais e objetivos de descarbonização.
							</li>
							<li>
								Há reconhecimento de normativos desatualizados e genéricos, que sujeitam qualquer
								alteração exterior a controlo municipal.
							</li>
							<li>
								Critica‑se a tendência política para "curto‑circuitar" processo em vez de reformar
								os mecanismos de controlo.
							</li>
							<li>
								Critérios de avaliação em património são considerados antiquados e vagos, remetendo
								para decretos antigos e subjetividade pouco enquadrada.
							</li>
						</ul>
					</li>
					<li>
						<b>Simplificação processual, novas construções e obrigações de renováveis</b>
						<p><b>Conclusão:</b></p>
						<p>
							Normas podem atuar como catalisadores ao impor integração de soluções renováveis em
							novas construções e reabilitações. Já existem algumas obrigações (principalmente via
							solar térmico), bem como figuras de "obras de escassa relevância", mas o quadro é
							incompleto e desigual, havendo espaço para clarificação.
						</p>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								Sugere‑se reforçar a obrigatoriedade de integração de soluções renováveis em nova
								construção.
							</li>
							<li>
								Já existem exigências de integração de renováveis (muitas vezes cumpridas com solar
								térmico).
							</li>
							<li>
								Algumas pequenas soluções (como pequenas turbinas em quintais) são já qualificadas
								como de escassa relevância urbanística, mas falta sistematização.
							</li>
						</ul>
					</li>
				</ol>
			</li>
			<li>
				<b>Comunidades de Energia: Enquadramento Legal, Barreiras e Capacidade Institucional</b>
				<ol>
					<li>
						<b>
							Definições legais de comunidades de energia e desalinhamento com diretivas europeias
						</b>
						<p><b>Conclusão:</b></p>
						<p>
							Há desalinhamento relevante entre a legislação nacional e as diretivas europeias sobre
							comunidades de energia:
						</p>
						<ul>
							<li>Quem pode ser comunidade;</li>
							<li>Quadro de atividades;</li>
							<li>Exigência de possuir uma UPAC própria;</li>
							<li>
								Isto restringe artificialmente o universo de entidades que poderiam ser reconhecidas
								como comunidades e limita a diversidade de modelos.
							</li>
						</ul>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								Distinção entre autoconsumo coletivo (sem entidade jurídica obrigatória) e
								comunidades de energia (entidade jurídica própria sem fins lucrativos).
							</li>
							<li>
								A transposição nacional exige que a comunidade tenha uma UPAC no seu nome, excluindo
								cooperativas e distribuidores cooperativos que não detêm instalações próprias.
							</li>
							<li>
								A legislação portuguesa reduz o âmbito das comunidades sobretudo à produção local,
								quando as diretivas permitem também mobilidade elétrica, reabilitação de edifícios e
								outros serviços.
							</li>
							<li>
								Muitas iniciativas apresentadas como "comunidade" são na prática autoconsumo
								coletivo empresarial, sem controlo cidadão.
							</li>
							<li>
								A alteração legislativa para corrigir estas distorções está pendente há cerca de um
								ano.
							</li>
						</ul>
					</li>
					<li>
						<b>Barreiras práticas, administrativas e sociais às comunidades de energia</b>
						<p><b>Conclusão:</b></p>
						<p>A expansão das comunidades de energia enfrenta fortes barreiras:</p>
						<ul>
							<li>Falta de conhecimento e capacidade técnica nas associações e autarquias;</li>
							<li>Regras restritivas em condomínios;</li>
							<li>Receio de problemas de contratação pública;</li>
							<li>Atrasos administrativos e operacionais;</li>
							<li>Limitações de rede e critérios de proximidade.</li>
						</ul>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								Associações e autarquias carecem de apoio técnico e informação sobre como estruturar
								projetos.
							</li>
							<li>
								A exigência de 75% de aprovação dos condóminos para instalar painéis e fazer
								Autoconsumo Coletivo em telhados é considerada bloqueadora.
							</li>
							<li>
								Autarquias temem riscos futuros de responsabilização por questões de contratação
								pública, travando iniciativas.
							</li>
							<li>
								Atrasos das empresas e Municípios (meses para respostas técnicas e autorizações)
								minam a confiança e a viabilidade dos projetos.
							</li>
							<li>
								Condicionamentos de rede e critérios de proximidade (2 km, por Posto de
								Transformação) criam paradoxos e podem levar à exclusão de membros à medida que a
								comunidade cresce.
							</li>
							<li>
								Modelos de uso de telhados públicos com critérios não económicos (como na Holanda e
								Bélgica) são apontados para mitigar barreiras, mas ainda pouco explorados em
								Portugal.
							</li>
						</ul>
					</li>
					<li>
						<b>Novos modelos tecnológicos e geográficos: comunidades, VNM e eólico urbano</b>
						<p><b>Conclusão:</b></p>
						<p>
							Para desbloquear o potencial renovável em áreas urbanas é necessário complementar o
							solar fotovoltaico com:
						</p>
						<ul>
							<li>Comunidades de energia e autoconsumo coletivo robustos e simplificados;</li>
							<li>Virtual network metering (VNM) para gerir excedentes temporalmente;</li>
							<li>Valorização de excedentes via tarifas apropriadas;</li>
							<li>
								Exploração de eólico em contexto urbano e portuário, apesar de barreiras culturais.
							</li>
						</ul>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								Comunidades de energia e modelos coletivos com valorização de excedentes são
								centrais para viabilizar renováveis em meio urbano.
							</li>
							<li>
								VNM é relevante para permitir injetar de dia e consumir à noite, apesar de questões
								de oportunidade.
							</li>
							<li>
								Eólico em áreas urbanas e portuárias é sugerido como opção, com exemplos de portos
								na Dinamarca, mas enfrenta resistências sociais e políticas (tema "tabu").
							</li>
						</ul>
					</li>
				</ol>
			</li>
			<li>
				<b>Complexidade Regulatória, Comunicação e Articulação Energia–Urbanismo</b>
				<ol>
					<li>
						<b>Complexidade regulatória, compreensão e comunicação intersectorial</b>
						<p><b>Conclusão:</b></p>
						<p>
							A discussão sobre tarifas, modelos de autoconsumo, regulação e planeamento da rede é
							tecnicamente complexa e pouco acessível a urbanistas e outros profissionais não
							especializados. Esta complexidade compromete a articulação entre planeamento
							energético e urbano e leva a subaproveitamento do potencial das áreas
							artificializadas.
						</p>
						<p><b>Pontos de Discussão:</b></p>
						<ul>
							<li>
								A conversa técnica foi qualificada como "críptica" para urbanistas, revelando falhas
								de tradução entre o mundo energético e o urbanístico.
							</li>
							<li>
								Há reconhecimento da necessidade de diferentes perfis especializados
								(económico/tarifário e tecnológico/regulatório) e de mediação entre esses mundos.
							</li>
						</ul>
					</li>
				</ol>
